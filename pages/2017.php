					<p>14:10 - 14:40 <strong>New work - new legal entities: the birth of new economic spaces</strong><br><span class="small"><strong><a href="#esko-kilpi" class="scroll-to">Esko Kilpi</a></strong> (Researcher, Author and Lecturer on post-industrial work)</span></p>
						<p><a href="#abstract-kilpi" class="btn small inline lightbox">Show Abstract</a></p>
						<div id="abstract-kilpi" class="hidden">
							<div class="inline-content">
								<p>The new technological environment of business is marked by conflicting constraints and value-creating relationships that change rapidly and constantly. It is a complex environment. In complex environments, the way to vitality and resilience is a continuous recombination of successful elements to create new versions, some of which may thrive.</p>
								<p>As a result, the operating system of work is starting to change in a radical way. Success is based on continuous redefinition of the organization itself. It is about recombining options and contributions in a competitive and cooperative network environment.</p>
								<p>The concept of economic space represents a new logic of organizing based on neither the traditional market nor a process. Whereas processes involve relations based on dependence and markets involve relations based on independence, the new economic spaces involve relations of dynamic interdependence.</p>
								<p>Post-blockchain smart contracts make possible, in economically viable ways, that person A can be part in the work/learning of person B. B again plays part in the work/learning of person C, who plays part in the work/learning of A. Work is by default networked cognition. Value creation is event-based and contextually highly interdependent cooperation.</p>
							</div>
