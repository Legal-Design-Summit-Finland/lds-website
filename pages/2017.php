				<h4>3. Privacy by (Legal) Design</h4>
				<blockquote>
					<p>17:30 - 18:00 <strong>Designing for the Internet of (Legal) Things</strong><br><span class="small"><strong><a href="#jose-f-torres" class="scroll-to">Jose Torres</a></strong> (Director of the Center for Innovation in Law, the University Sergio Arboleda)</span></p>
					<p>18:00 - 18:30 <strong>When Legal Design Meets the Semantic Web: Rethinking how we Interact with Data Protection</strong><br><span class="small"><strong><a href="#arianna-rossi" class="scroll-to">Arianna Rossi</a></strong> (Researcher, University of Bologna)</span></p>
						<p><a href="#abstract-rossi" class="btn small inline lightbox">Show Abstract</a></p>
						<div id="abstract-rossi" class="hidden">
							<div class="inline-content">
								<p>Nobody reads privacy policies. They are long, difficult, tedious legal documents. However, the law requires individuals to be informed about how their personal data is handled and competent about their privacy preferences. So how can legal requirements and actual users’ behaviors be reconciled? The forthcoming General Data Protection Regulation (GDPR) suggests icons to convey data practices in a more understandable manner. I will present a framework where legal semantic web technologies interact with visualizations and human-centered design, with the aim of helping individuals to flow through the complexity of privacy information and to exercise more control over their data</p>
