								<h4>Designed to Deliver</h4>
								<p>Whether you’re summarizing or trying to grasp the essence of complicated legal information, visualization is an incredibly powerful method of getting a message across convincingly, clearly and quickly, while improving both comprehension and retention. But is there a difference between introducing design in the legal world and legal design? We -at Houthoff- believe the differentiating element is adding behavioral psychology to the mix. In this keynote Sarah van Hecke will provide some insights on how legal design is defined and used at on of the top Dutch law firms while outlining how this combination of disciplines (lawyer, legal design consultant, designer) can create interesting and unexpected solutions.</p>
