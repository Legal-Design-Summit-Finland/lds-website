								<h4>The main abstract will be this:</h4>
								<p>Legal Design is at a crossroads. Often lawyers start engaging with design only in terms of visuals and communication — how to make documents look better and function more efficiently. There are more ambitious types of legal design though — redesigning not just single work products, but entire services, organizations, and systems. In this presentation, I will lay out a vision of how legal designers may expand their ambitions to more complex design projects, that can make our legal system fundamentally more human-centered.</p>
