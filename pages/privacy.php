		<h2>Privacy Policy</h2>
		<p>Updated 1.6.2019</p>

		<p>This Privacy Policy describes how personal data is processed by Legal Design Finland ry. Legal Design Finland ry is the owner and the organizer of the Legal Design Summit 2019 and therefore the controller.</p>

		<p>We process your data responsibly and we want to be worthy of your trust. You can influence the way in which your data is processed. In this Privacy Policy, we describe your rights and how to exercise them.</p>

		<p>Your data will be stored when you reserve a ticket through our ticket store or otherwise interact with the organizers of the Legal Design Summit. We process your data when producing the event and for the purpose of offering you a wonderful experience. In the future, we want to create increasingly better experiences, and the data we collect will help us to understand what sort of audience is interested in the Legal Design Summit and how can we serve them better.</p>

		<h4>1.	From where do we collect data?</h4>


		<p>We collect data about you, for example, when you:</p>
		<p>-	register as a participant (reserve a ticket)</p>
		<p>-	contact us</p>
		<p>-	give us a marketing permission and receive messages from us (note that you can always cancel your subscription at any moment)</p>
		<p>-	take part in some of our marketing campaigns or marketing surveys or an opinion poll we organize</p>
		<p>-	give us feedback, leave us a message to contact you or otherwise get in touch with the Legal Design Summit</p>
		<p>-	are a contact person or a representative of one of our stakeholders</p>
		<p>-	share, publish, comment on something or send a message in our social media channels. We carry out social media monitoring on general level to understand our participants better and to develop our marketing.</p>


		<h4>2.	What kind of data do we collect?</h4>


		<p>We get some of the data directly from you, for instance, when you register to the event.</p>

		<p>We collect the following data:</p>

		<p>-	Name and contact details:</p>
		<p>We collect your first and last name, email address, billing address and other similar contact details.</p>

		<p>-	Demographic data:</p>
		<p>We collect other descriptive data, such as occupation, and country to learn more about our participants.</p>

		<p>-	Customer relationship data:</p>
		<p>We collect data, for example, on your registration to our events. We collect information on customer feedback and other customer contacts.</p>

		<p>-	Background data:</p>
		<p>We collect data, for example, on your background if you attend to the Brainfactory. We need this data to evaluate the candidates and also to form the groups. Thisway we can offer you the best possible Brainfactory experience.</p>

		<p>-	Data on representation:</p>
		<p>We collect information about your position in the company or other organisation you represent, as well as the name and the contact details of the company or organisation in question, especially when you interact with us as a representative of your company or other organisation or stakeholder group. We also collect other information necessary for the management of the relationship between Legal Design Summit and the company or organisation you represent.</p>

		<p>-	Market surveys and opinion polls:</p>
		<p>We collect data in the interest of developing our event on the basis of the results of these market surveys and polls.</p>

		<p>-	Data on interest:</p>
		<p>We collect data on your interests related to the event, e.g. on what you would like to see next year.</p>

		<p>-	Marketing permissions and prohibitions</p>

		<p>-	Visual recordings:</p>
		<p>We take photographs and record video at all of our events. If we photograph you in particular and not the whole audience or ambiance, we will ask for your consent to publish the image. Your picture may also be recorded by our live streaming cameras.</p>


		<p>You can influence the type of data we collect. However, participation in our events requires that you submit your personal data. If you decide not to submit your personal data to us, we may be unable to provide all the possibilities of our events to you.</p>



		<h4>3.	What is the data used for?</h4>


		<p>We process your data in order to organize our events. We also process your data if it is necessary for the realization our legitimate interest.</p>

		<p>“Legitimate interest” refers to our actions that are allowed by law and which are in direct relation to our operations. We may act on the grounds of legitimate interest where a significant and pertinent relationship exists between the participant and us. This means that the relationship has been formed for the purpose of participating in our events itself or in the organization of the event in the role of e.g. audience, a member of organizers or a member of partnering organizations.</p>

		<p>In assessing legitimate interest, both the interests of the participant and ours are evaluated, taking in to account the realization of fundamental rights, the nature and purpose of use of the data collected, and data security. This process has been explained e.g. by the European Data Authorities and we follow these established guidelines.</p>

		<p>The participant has the right to object to processing that occurs on the basis of a legitimate interest.  We explain this further in the section 8. In some cases, we may ask your consent for processing your data for example by using opt-in check boxes in registration or other forms.</p>
 
		<p>We also process your data to better be able to market our event. We may also process your data when conducting marketing surveys and opinion polls to gain a better understanding of our participants and thereby make the event better. We target our content to provide you with information about our event that is as relevant as possible. The targeting of our content is based on interests and information we have collected. For example, we might send different information to investors than startup entrepreneurs.</p>

		<p>We process the data for analysis, for compiling statistics and reporting, again, mainly to develop our event and to improve the participant experience.</p>



		<h4>4.	How long do we store your data?</h4>


		<p>We do not store your data any longer than necessary for the purposes described in this Privacy Policy and to comply with any mandatory legislation. We store your data for 2,5 years. Each organizing year after the event we delete the last years participants. We may store the data longer, if you give us your consent for that.</p>

		<p>Please note that regardless of what has been said above, we may make an exception in retention period when a legal claim is made against us or when the handling of matters related to such a claim is incomplete or when the law requires us to do so.</p>


		<h4>5.	Who can process your data, and do we disclose it to third parties?</h4>


		<p>Your data is processed within the Legal Design Summit team. We may disclose your data to the authorities, for example, if the law requires us to do so. To fulfil our contractual obligations, we may disclose personal data to third parties. Such third parties include for example our partner offering the ticket service. These parties may also act as the controller in terms of personal data needed for the registration. We advise you to read the data protection policies of these parties.</p>

		<p>However, we do not, as a rule, disclose your data to third parties in such a manner that they would be able to use it for other purposes than those defined by us. When using subcontractors or service providers we obligate them with appropriate contractual means to maintain a level of information security that is adequate to protect your data and to comply with personal data legislation.</p>


		<h4>6.	Do we transfer your data beyond the borders of the EU or EAA (“third countries”)?</h4>

		<p>Due to using for example Facebook and our ticket registration services, your data may be transferred to the United States. Our service providers in the United States comply with rules of the Privacy Shield agreement between the EU and the United States, to ensure an adequate level of data protection. You can read more about the Privacy Shield agreement between the EU and the United States on the website of the European Commission.</p>


		<h4>7.	How can you influence the processing of your personal data?</h4>

		<p>You can control how we collect and use your data.</p>

		<p>You are entitled to influence the processing of your data in the following ways:</p>


		<p>-	You have the right to forbid the processing of your personal data for the purposes of direct marketing at any time (right to object). Please note that you may receive marketing communication from us in relation to your work or area of responsibility.</p>

		<p>-	In some situations, you may have the right, for reasons related to special personal circumstances, to object to the processing of your personal data beyond the purposes of direct marketing when it is done by virtue of our legitimate interest.</p>

		<p>-	You have the right to request access to data concerning you or to receive confirmation that we do not process data concerning you (right to access).</p>

		<p>-	In some cases, you have the right to receive the personal data you provided to us in digital format so that you can transfer it to another controller (right to portability).</p>

		<p>-	You have the right to have data rectified (right to rectification).</p>

		<p>-	In certain situations, you may have the right to restrict processing (right to restriction of processing). In other words, if you dispute the accuracy of your data, you have the right to restrict its processing for period of time that allows us to ensure that your data is accurate.</p>

		<p>-	You have the right to have your data erased in certain situations (right to be forgotten). For example, when processing of your personal data is no longer necessary for the purposes for which it was collected, or the processing was based on your consent and you wish to withdraw that consent. Please note if your personal data is necessary for the purpose of, for instance, the registration of your participation to the event, we cannot remove your data.</p>

