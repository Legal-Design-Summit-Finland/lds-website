					<p>Brainfactory is an interdisciplinary hands-on workshop on legal design. No prior knowledge of legal design is required but the objective of the Brainfactory is to serve as a taster of what legal design may be in practise. During the weekend, participants will obtain practical tools on how to solve a legal problem through design. Brainfactory has two tracks: public and private track.</p>

					<h4>Application</h4>
					<p>The application to the Brainfactory will open in June. The participation is limited and we will inform you of admittance as soon as possible.</p>

					<h4>Tracks</h4>
					<p>The Brainfactory will have two tracks: private and public. In the private track, the legal design problem to be solved relates to private law. It is thus more of commercial and consumer law in nature. In the public track, on the other hand, the legal design problem touches upon issues relevant in public law, like the right to privacy or access to justice. When making the team formation, we try to take into account which track you have marked as preferred in your application.</p>

					<h4>Teams</h4>
					<p>During the Brainfactory you will not only get to learn about legal design but you also get new friends! The workshop takes place in interdisciplinary teams of four people. The teams are formed on Friday.</p>

					<h4>For whom?</h4>
					<p>Brainfactory is meant for legal design enthusiasts with background in law, design, business or technology. Both students and professionals are welcome to apply.</p>

