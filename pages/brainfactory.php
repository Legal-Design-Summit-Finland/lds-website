			<h4>10.-12.9.2019</h4>
			
			<h3>Would you like to experience legal design in action?</h3>
			
			<p class="bullet large">An interdisciplinary legal design workshop that takes place preceding the summit.</p>
			<p class="bullet large">Attendees obtain hands-on experience in combining law and design, mentored by legal design professionals.</p>
			<p class="bullet large">Furthermore, the teams will have the opportunity to receive feedback from the Brainfactory Board.</p>
			
			<div class="row no-gutters">
				
				<div class="col-lg-8 col-lg-offset-2">
					
					<p>Brainfactory is an interdisciplinary hands-on workshop on legal design. No prior knowledge of legal design is required but the objective of the Brainfactory is to serve as a taster of what legal design may be in practise. During the intensive 3-days, participants will obtain practical tools on how to solve a legal problem through design. Brainfactory has three different tracks which will be published in August for the selected participants.</p>

					<h4>Application</h4>
					<p>The application to the Brainfactory is now open. The participation is limited and we will inform you of admittance latest in the end of June, 2019. <a href="https://forms.gle/uU1my3zm8dwSETXi8" target="_blank">Apply here!</a></p>

					<h4>Teams</h4>
					<p>During the Brainfactory you will not only get to learn about legal design but you also get new friends! The workshop takes place in interdisciplinary teams of four people. The teams are formed on Day 1.</p>

					<h4>For whom?</h4>
					<p>Brainfactory is meant for legal design enthusiasts with a background in law, design, business or technology. Both students and professionals are welcome to apply.</p>

