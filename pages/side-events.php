					<h3>SIDE EVENT BY PORTYR:<br><span class="small">Generating a common language for GDPR</span></h3>
					<img src="images/events/portyr.png">
					<div class="justify-left">
						<p>"GDPR in three simple steps..." "Complying with GDPR is easy..." – Let's face the truth: Tackling GDPR is complex. A solution demands an adequate holistic approach.</p>
						<p>The startup Portyr tackles this complex challenge with a tight team of experts in the fields of technology, law and design. The team generates a common, open language and translates GDPR into actionable steps, making the General Data Protection Regulation an asset to both companies and individuals.</p>
						<p>For the LDS participants, Portyr opens their doors to explore and discuss together the generation of a common industry language to describe personal data and its potential impact, especially from a company perspective. The workshop builds on a design-led approach, encouraging visual communication such as mapping out services and stakeholders which are involved in the GDPR. Further, the workshop offers an outline of a potential open, common language as a foundation to extend the discussion of current and future prospects of the GDPR. The outcomes of the workshop will be summarized and shared with the participants.</p>
						<p>Before starting with the practical work, a light breakfast will be offered at 9am.</p>
